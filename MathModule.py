#Math built-in function(built in functions do not need importing of a module)
x = -1
print(abs(x))

print(max(69, 34,1))

print(min(69, 70, 71))

print(pow(2,3))

#Math functions using math module
import math as m

print(m.fabs(-2))

print(m.ceil(2.1))

print(m.floor(2.9))

print(m.exp(1))

print(m.log(2.718))

print(m.log(8,2))

print(m.sqrt(16))

print(m.sin(30))

print(m.asin(1/30))

print(m.degrees(m.pi/3))

print(m.radians(360))